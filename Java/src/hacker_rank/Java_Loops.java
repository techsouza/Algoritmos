package hacker_rank;

import java.util.*;

public class Java_Loops {

    public static void main(String[] args) {

        try (Scanner leia = new Scanner(System.in)) {
            int numero = leia.nextInt();
        
        
        for(int i=1; i<=10; i++){
            
            System.out.println(numero + " x " + i + " = " +i*numero);
        }
        }

        
    }
    
}
